package com.example.conversormoedas;

public class Currency {
    private String code;
    private String name;
    private String high;
    private String low;
    private String bid;

    public String getCode() {
        return code;
    }

    public String getName() {
        return name;
    }

    public String getHigh() {
        return high;
    }

    public String getLow() {
        return low;
    }

    public String getBid() {
        return bid;
    }

}
